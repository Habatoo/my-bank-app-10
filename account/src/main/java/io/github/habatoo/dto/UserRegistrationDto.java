package io.github.habatoo.dto;

import lombok.AllArgsConstructor;
import lombok.Data;

import java.math.BigDecimal;
import java.time.LocalDate;

/**
 * Объект передачи данных (DTO) для регистрации нового пользователя в системе.
 * <p>
 * Данный класс собирает первичную информацию, необходимую для создания
 * учетной записи и открытия начального счета пользователя.
 * </p>
 */
@Data
@AllArgsConstructor
public class UserRegistrationDto {

    /**
     * Уникальный логин (идентификатор) пользователя.
     * Используется для входа в систему и связывания с JWT-токеном.
     */
    private String login;

    /**
     * Полное имя пользователя.
     * Обычно включает в себя имя и фамилию для корректного отображения в профиле.
     */
    private String name;

    /**
     * Дата рождения пользователя.
     * Используется для проверки возрастных ограничений или в целях верификации.
     */
    private LocalDate birthDate;

    /**
     * Начальная сумма (баланс), зачисляемая при открытии счета.
     * Позволяет установить стартовый капитал пользователя в рамках промо-акций
     * или первичного пополнения.
     */
    private BigDecimal initialSum;
}
