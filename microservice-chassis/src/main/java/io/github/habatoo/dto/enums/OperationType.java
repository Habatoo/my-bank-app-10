package io.github.habatoo.dto.enums;

/**
 * Перечисление доступных действий с наличными средствами.
 * <p>
 * Используется для определения типа транзакции в операциях пополнения
 * или снятия виртуальных денег со счета пользователя.
 * </p>
 */
public enum OperationType {

    /**
     * Операция внесения средств на счет (Депозит).
     * <p>
     * Приводит к увеличению текущего баланса пользователя.
     * Соответствует кнопке «Положить» в пользовательском интерфейсе.
     * </p>
     */
    PUT,

    /**
     * Операция снятия средств со счета (Выплата).
     * <p>
     * Приводит к уменьшению текущего баланса пользователя.
     * Требует обязательной проверки на наличие достаточной суммы на счету.
     * Соответствует кнопке «Снять» в пользовательском интерфейсе.
     * </p>
     */
    GET
}
